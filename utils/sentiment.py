# کلمات نفی
NEGATION_WORDS = {'نه', 'بدون', 'هیچ', 'نفی', 'عدم', 'غیر', 'نا', 'نپذیرفت', 'نشد', 'نگردید'}

# کلمات تشدیدکننده (تأکید)
INTENSIFIERS = {'بسیار', 'خیلی', 'فوق العاده', 'شدیدا', 'کاملا', 'به شدت', 'ابدی'}

def analyze_sentiment(text: str) -> float:
    if not text:
        return 0.0
    
    text = text.lower()
    text = re.sub(r'[^\w\s]', ' ', text)
    words = text.split()
    
    total_score = 0
    negation_flag = False
    intensity = 1.0
    
    for i, word in enumerate(words):
        # نفی
        if word in NEGATION_WORDS:
            negation_flag = True
            continue
        
        # تشدیدکننده
        if word in INTENSIFIERS:
            intensity = 1.5
            continue
        else:
            intensity = 1.0
        
        score = 0
        # بررسی مثبت
        for key, val in POSITIVE_WORDS.items():
            if key in word or word in key:
                score = val
                break
        # بررسی منفی
        if score == 0:
            for key, val in NEGATIVE_WORDS.items():
                if key in word or word in key:
                    score = -val
                    break
        
        if score != 0:
            if negation_flag:
                score = -score
                negation_flag = False
            total_score += score * intensity
    
    if len(words) == 0:
        return 0.0
    max_possible = len(words) * 3
    normalized = total_score / max_possible
    return max(-1.0, min(1.0, normalized))
